<h4><a name="Home-WhatisRingsideSocialApplicationServer%3F"></a>What is Ringside Social Application Server?</h4>

<p>Open source social application server, providing every request with the context of the user, user data and the users network.&nbsp; Thus giving every application the capability to be more social. Combining your application with Ringside Social Application Server can turn an application into a social network of it's own giving you the breadth of a Facebook with a platform open to as many other applications, websites and users you desire.</p></td><td class="confluenceTd"  valign="top" width="50%">

<h4><a name="Home-AreyouOpenSource%3FWhy%3F"></a>Are you Open Source? Why?</h4>

<p>Absolutely 100% open source based on the LGPL license. We are committed to a 100% transparent development philosophy and organization. Great software is best done in the open so it can become great software through trials and tribulations. We saw the power of community it unlocked at JBoss and Red Hat and we just fundamentally believe in it. Either way its Open Source today and its not changing tomorrow. Enjoy&#33; </p></td></tr></tbody></table><table class="sectionMacro" border="0" cellpadding="5" cellspacing="0" width="100%"><tbody><tr><td class="confluenceTd"  valign="top" width="55%">

